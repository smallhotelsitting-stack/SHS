const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const pathLang = window.location.pathname.split('/')[1] as Language;
    if (['en', 'es', 'fr', 'de', 'it', 'pt'].includes(pathLang)) {
      localStorage.setItem('language', pathLang);
      return pathLang;
    }
    const saved = localStorage.getItem('language');
    return (saved as Language) || 'en';
  });

  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  const setLanguage = (lang: Language) => {
    const currentPath = window.location.pathname;
    const currentLang = currentPath.split('/')[1];
    const pathWithoutLang = currentPath.replace(`/${currentLang}`, '') || '/';
    window.location.href = `/${lang}${pathWithoutLang}${window.location.search}`;
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  const getLocale = (): string => {
    const localeMap: Record<Language, string> = {
      en: 'en-US',
      es: 'es-ES',
      fr: 'fr-FR',
      de: 'de-DE',
      it: 'it-IT',
      pt: 'pt-PT'
    };
    return localeMap[language];
  };

  const formatDate = (date: string | Date): string => {
    const dateObj = typeof date === 'string' ? new Date(date) : date;
    return dateObj.toLocaleDateString(getLocale(), {
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    });
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, formatDate, getLocale }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

